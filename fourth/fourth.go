package main

import "fmt"

func main() {
	FirstOperand := 5
	SecondOperand := 3
	fmt.Println("FirstOperand, SecondOperand:", FirstOperand, SecondOperand)
	fmt.Println("a + b =", FirstOperand + SecondOperand)
	fmt.Println("a - b =", FirstOperand - SecondOperand)
	fmt.Println("a * b =", FirstOperand * SecondOperand)
	fmt.Println("a / b =", FirstOperand / SecondOperand)
	fmt.Println("a % b =", FirstOperand % SecondOperand)
}
